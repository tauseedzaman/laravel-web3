const contract = new web3.eth.Contract(contractAbi, contractAddress);


async function write(toAccount, amount) {
    // const amount = 10; // Replace with the amount of tokens you want to send
    const amountInWei = web3.utils.toWei(amount.toString(), 'ether');

    // Encode the 'transfer' function call data
    const data = contract.methods.transfer(toAccount, amountInWei).encodeABI();

    // Build the transaction object for the contract interaction
    const gasPrice = await web3.eth.getGasPrice();
    // Estimate gas needed for the transaction
    const estimatedGas = await web3.eth.estimateGas({
        from: fromAccount,
        to: contractAddress,
        data: data,
    });

    // Build the transaction object for the contract interaction with the estimated gas
    const transactionObject = {
        from: fromAccount,
        to: contractAddress,
        data: data,
        gas: estimatedGas, // Use the estimated gas
        gasPrice: gasPrice,
    };

    // Sign the transaction with the private key
    const signedTransaction = await web3.eth.accounts.signTransaction(
        transactionObject,
        private_key
    );

    // Send the signed transaction
    const receipt = await web3.eth.sendSignedTransaction(signedTransaction.rawTransaction);
    return receipt.transactionHash
}


